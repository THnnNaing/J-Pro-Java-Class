package time;
public class UsingTime {
	
	public static void main(String[] args) {
		time t1=new time();
		t1.inputTime();
		
		time t2= new time(3,45,50);
		time t3= new time(7800);
		
		time totT1= t1.addTime(t2);
		time totT2= t1.addTime(t2,t3);
		
		System.out.print("Time t1 : ");t1.showTime();
		System.out.print("Time t2 : ");t2.showTime();
		System.out.print("Time t3 : ");t3.showTime();
		System.out.print("Time totT1 : ");totT1.showTime();
		System.out.print("Time totT2 : ");totT2.showTime();
		
	}
	
}
