// Relational_Op.java
class Relational_Op {
	public static void main(String[] args) {
		int a=10,b=20,c=30,d=40;

		System.out.println("10<20 :" + (a<b)); //true
		System.out.println("10>=20 :" + (a>=b)); //false
		System.out.println("30!=40 :" + (c!=d)); //true
		System.out.println("30==40 :" + (c==d)); //false
		
	}
}