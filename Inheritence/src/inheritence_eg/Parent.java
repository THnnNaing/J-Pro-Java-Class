package inheritence_eg;

public class Parent {
	private int a; //own
	protected int b; //can be used by child
	
//	public Parent() {
//		
//	}
	
	public Parent () {
		this.a=a;
		this.b=b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	
	
}
